export function I18nProvider({ children }) {
  const [locale, setLocale] = useState('en');

  // Sync active language selection from localStorage on mount
  useEffect(() => {
    const stored = localStorage.getItem('trio_lang');
    if (stored && ['en', 'si', 'ta'].includes(stored)) {
      setLocale(stored);
    }
  }, []);

  const changeLocale = (newLocale) => {
    if (['en', 'si', 'ta'].includes(newLocale)) {
      setLocale(newLocale);
      localStorage.setItem('trio_lang', newLocale);
    }
  };

  const t = (key, brandId = 'happybuy') => {
    // If translation doesn't exist, return original key
    let translation = DICTIONARY[locale]?.[key] || key;

    // Apply Modabella lowercase rule
    if (brandId === 'modabella' && locale === 'en') {
      return translation.toLowerCase();
    }
    return translation;
  };

  return (
    <I18nContext.Provider value={{ locale, t, changeLocale }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider');
  }
  return context;
}
